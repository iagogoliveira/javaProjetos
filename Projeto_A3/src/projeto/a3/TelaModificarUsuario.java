    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableUsers.getModel();
        modelo.setNumRows(0);
        UsuarioDAO uMet = new UsuarioDAO();

        for (Usuario us : uMet.read()) {

            modelo.addRow(new Object[]{
                us.getId(),
                us.getNome(),
                us.getNomeUser(),
                us.getEmail(),
                us.getSenha(),
                us.getTelefone()
            });
        }
    }

