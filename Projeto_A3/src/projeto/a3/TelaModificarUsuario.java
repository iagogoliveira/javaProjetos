    private void removerBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerBotaoActionPerformed
        // TODO add your handling code here:
        if (jTableUsers.getSelectedRow() != -1) {
            Usuario user = new Usuario();
            UsuarioDAO uMet = new UsuarioDAO();

            user.setId((int) jTableUsers.getValueAt(jTableUsers.getSelectedRow(), 0));
            uMet.deletar(user);

            nomeString.setText("");
            loginString.setText("");
            emailString.setText("");
            senhaString.setText("");
            telefoneString.setText("");

            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um usu√°rio para exclus√£o");
        }
    }//GEN-LAST:event_removerBotaoActionPerformed

